letter = input('\nEnter a letter: ')

if letter =='B' or letter =='b':
    print ("Blue\n")
elif letter == 'R' or letter == 'r':
    print ("Red\n")
elif letter == 'G' or letter == 'g':
    print ("Green\n")
elif letter == 'Y' or letter == 'y':
    print ("Yellow\n")
else:
    print ("Unknown Color")